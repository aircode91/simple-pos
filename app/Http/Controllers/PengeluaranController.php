<?php

namespace App\Http\Controllers;

use Illuminate\Http\Request;
use App\Models\Pengeluaran;

class PengeluaranController extends Controller
{
    public function index()
    {
        return view('pengeluaran.index');
    }

    public function data()
    {
        $pengeluaran = Pengeluaran::orderBy('id', 'desc')->get();

        return datatables()
            ->of($pengeluaran)
            ->addIndexColumn()
            ->addColumn('created_at', function ($pengeluaran) {
                return tanggal_indonesia($pengeluaran->created_at, false);
            })
            ->addColumn('amount', function ($pengeluaran) {
                return format_uang($pengeluaran->amount);
            })
            ->addColumn('action', function ($pengeluaran) {
                return '
                    <button type="button" onclick="editForm(`' . route('pengeluaran.update', $pengeluaran->id) . '`)" class="btn btn-sm btn-info "><i class="fa fa-pencil"></i></button>
                    <button type="button" onclick="deleteData(`' . route('pengeluaran.destroy', $pengeluaran->id) . '`)" class="btn btn-sm btn-danger "><i class="fa fa-trash"></i></button>
                ';
            })
            ->rawColumns(['action'])
            ->make(true);
    }

    /**
